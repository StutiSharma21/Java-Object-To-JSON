package org.example;

public class Plant{
    private String name;
    private int leaves;
    private int flowers;

    public void setName(String name) {
        this.name = name;
    }

    public void setLeaves(int leaves) {
        this.leaves = leaves;
    }
    public void setFlowers(int flowers) {
        this.flowers = flowers;
    }

    public String getName() {
        return name;
    }

    public int getLeaves() {
        return leaves;
    }

    public int getFlowers() {
        return flowers;
    }

}
